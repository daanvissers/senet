
public class Player {
	
	private String name;
	private String colorSign;
	
	public Player(String name) {
		this.setName(name);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getColorSign() {
		return colorSign;
	}

	public void setColorSign(String colorSign) {
		this.colorSign = colorSign;
	}

}
